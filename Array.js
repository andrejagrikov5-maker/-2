// Исходные данные

const prices = [1200, 3400, 560, 999, 2500, 4100, 800];
const categories = ["телефон", "ноутбук", "наушники", "телефон", "планшет", "ноутбук", "наушники"];
const discounts = [10, 0, 15, 5, 20, 0, 10];

// =====================================================
// 1. Перебор массива (forEach)
// =====================================================
// TODO: Выведите все цены в формате:
// "Цена товара: 1200 ₽"


prices.forEach((price, index) => {
    console.log(`Цена товара( ${categories[index]} ): ${price} ₽`);
});


// =====================================================
// 2. Преобразование массива (map)
// =====================================================
// TODO: Создайте новый массив pricesWithDiscount,
// где к каждой цене применена скидка из массива discounts
// Формула: цена - (цена * скидка / 100)


const pricesWithDiscount = prices.map((price, index) => {
    const discount = discounts[index];
    const DP = price * discount/ 100;
    return DP;
});

console.log("Цены со скидкой:", pricesWithDiscount);


// =====================================================
// 3. Фильтрация массива (filter)
// =====================================================
// TODO: Получите массив цен дороже 2000

const expensivePrices = prices.filter(price => price>2000);

console.log("Дорогие товары:", expensivePrices);


// =====================================================
// 4. Поиск элемента (find)
// =====================================================
// TODO: Найдите первую цену больше 4000

const firstVeryExpensive = prices.find(price => price>4000);

console.log("Первая цена больше 4000:", firstVeryExpensive);


// =====================================================
// 5. Проверка условий (some, every)
// =====================================================
// TODO: Проверьте, есть ли товары дешевле 1000

const hasCheapProducts = prices.some(price => price<1000);

// TODO: Проверьте, что все цены больше 0

const allPricesPositive = prices.every(price => price>0);

console.log("Есть ли дешёвые товары:", hasCheapProducts);
console.log("Все ли цены положительные:", allPricesPositive);


// =====================================================
// 6. Сортировка массива (sort)
// =====================================================
// TODO: Отсортируйте цены по возрастанию
// ВАЖНО: исходный массив prices изменять нельзя

const sortedPrices = [...prices].sort((a, b) => {
    return a - b; // сортировка по возрастанию
    // return b - a; // если нужно по убыванию
});

console.log("Отсортированные цены:", sortedPrices);

// ОБЪЯСНЕНИЕ:
// [...prices] - создаем КОПИЮ массива (чтобы не менять оригинал)
// sort - сортирует массив (изменяет его!)
// (a, b) - два элемента для сравнения
// если a - b < 0 - a станет перед b
// если a - b > 0 - b станет перед a
// если = 0 - порядок не меняется


// =====================================================
// 7. Работа с несколькими массивами (reduce)
// =====================================================
// TODO: Посчитайте общую стоимость всех товаров

const totalPrice = prices.reduce((sum, price) => {
    return sum + price;
}, 0);

console.log("Общая стоимость:", totalPrice);


// =====================================================
// 8. Работа со строками в массиве
// =====================================================
// TODO: Создайте массив уникальных категорий товаров
// Подсказка: includes()

const uniqueCategories = categories.reduce((result, category) => {
    if (!result.includes(category)) {
        // если категории еще нет в результате
        result.push(category); // добавляем ее
    }
    return result; // возвращаем обновленный результат
}, []);

console.log("Уникальные категории:", uniqueCategories);


